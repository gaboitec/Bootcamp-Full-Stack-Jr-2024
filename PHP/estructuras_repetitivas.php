<?php 

    //Estructuras repetitivas
    //For
    for($i = 0; $i < 5; $i++){
        print $i;
    }

    //While
    $contador = 1;
    while($contador<5){
        print $contador;
        $contador++;
    }

    $contador2 = 5;
    //Do While
    do{
        print $contador2;
        $contador2++;
    }while($contador2 <5);

?>